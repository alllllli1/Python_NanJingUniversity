#输入语句的一般形式：
a = input("输入提示：")
#返回值类型是str

#输出语句的一般形式：
b = print("1","2","...","n",sep=' ',end='\n')
#sep表示输出对象之间的分隔符，默认是空格；
#参数end的默认值为'\n'，表示print()函数输出完成后自动换行

